-- ===========================================
-- 2025年度源泉征收税额表（月额表）
-- 用于所得税源泉征收的计算
-- 令和７（2025）年度 源泉徴収税額表
-- ===========================================

-- 创建源泉征收税额表
CREATE TABLE source_withholding_tax (
    id BIGSERIAL PRIMARY KEY,
    min_amount INTEGER NOT NULL,
    max_amount INTEGER,
    tax_type VARCHAR(10) NOT NULL CHECK (tax_type IN ('甲', '乙')),
    dependents_count INTEGER,
    base_tax_amount INTEGER,
    tax_amount INTEGER,
    excess_tax_rate NUMERIC(6, 4),
    calculation_formula TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 添加表注释
COMMENT ON TABLE source_withholding_tax IS '2025年度源泉征收税额表（月额表），用于所得税源泉征收的计算';
COMMENT ON COLUMN source_withholding_tax.min_amount IS '金额下限（扣除社会保险费等后的工资等金额）';
COMMENT ON COLUMN source_withholding_tax.max_amount IS '金额上限（NULL表示无上限或使用公式计算）';
COMMENT ON COLUMN source_withholding_tax.tax_type IS '类型（甲：工资所得者的工资、薪金等；乙：工资所得者以外的工资、薪金等）';
COMMENT ON COLUMN source_withholding_tax.dependents_count IS '扶养亲族等数量（0-7人，仅适用于甲类型，乙类型为NULL）';
COMMENT ON COLUMN source_withholding_tax.base_tax_amount IS '基础税额（用于公式计算的基准税额）';
COMMENT ON COLUMN source_withholding_tax.tax_amount IS '税额（固定税额，当需要公式计算时为NULL）';
COMMENT ON COLUMN source_withholding_tax.excess_tax_rate IS '超过部分的税率（用于公式计算，如0.4084表示40.84%）';
COMMENT ON COLUMN source_withholding_tax.calculation_formula IS '计算公式（当需要公式计算时使用）';

-- 创建索引
CREATE INDEX idx_source_withholding_tax_type ON source_withholding_tax(tax_type);
CREATE INDEX idx_source_withholding_tax_amount_range ON source_withholding_tax(min_amount, max_amount);
CREATE INDEX idx_source_withholding_tax_dependents ON source_withholding_tax(tax_type, dependents_count);

